package MoviePlanet;

public class Genre
{
   private int genreID;
   private String name;

   public Genre()
   {
   }

   public int getGenreID()
   {
      return genreID;
   }

   public void setGenreID(int genreID)
   {
      this.genreID = genreID;
   }

   public String getName()
   {
      return name;
   }

   public void setName(String name)
   {
      this.name = name;
   }
}
